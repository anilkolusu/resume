package com.rootservice.bean;

public class CarDetails {

	private String distance;
	private String time;
	private String duration;
	private String travel;
	private String totalfuelcost;
	private String weartear;
	private String hotelcost;
	private String estimatedtolls;
	private String totalroundtripcost;
	
	
	public String getDistance() {
		return distance;
	}
	public void setDistance(String distance) {
		this.distance = distance;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getDuration() {
		return duration;
	}
	public void setDuration(String duration) {
		this.duration = duration;
	}
	public String getTravel() {
		return travel;
	}
	public void setTravel(String travel) {
		this.travel = travel;
	}
	public String getTotalfuelcost() {
		return totalfuelcost;
	}
	public void setTotalfuelcost(String totalfuelcost) {
		this.totalfuelcost = totalfuelcost;
	}
	public String getWeartear() {
		return weartear;
	}
	public void setWeartear(String weartear) {
		this.weartear = weartear;
	}
	public String getHotelcost() {
		return hotelcost;
	}
	public void setHotelcost(String hotelcost) {
		this.hotelcost = hotelcost;
	}
	public String getEstimatedtolls() {
		return estimatedtolls;
	}
	public void setEstimatedtolls(String estimatedtolls) {
		this.estimatedtolls = estimatedtolls;
	}
	public String getTotalroundtripcost() {
		return totalroundtripcost;
	}
	public void setTotalroundtripcost(String totalroundtripcost) {
		this.totalroundtripcost = totalroundtripcost;
	}
}
